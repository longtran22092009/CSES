void solve() {
    int n, m, k; cin >> n >> m >> k;

    vector <int> ppl(n), house(m);
    for (auto &x : ppl) cin >> x;
    for (auto &x : house) cin >> x;
    sort(all(ppl));
    sort(all(house));

    int ans = 0, i = 0, j = 0;
    while (i < n && j < m) {
        if (abs(ppl[i] - house[j]) <= k) {
            ++ans, ++i, ++j;
        }
        else if (ppl[i] + k < house[j]) ++i;
        else ++j;
    }

    cout << ans << endl;
}

